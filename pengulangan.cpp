#include <iostream.h>

using namespace std;

int main(){
//CONTOH FOR
cout<<"CONTOH FOR"<<endl;
for (int i = 0; i < 5; i++) {
  cout << i << "\n";
}

//CONTOH DO WHILE
cout<<"CONTOH DO WHILE"<<endl;
int a = 0;
	
	do {
		
			cout<<a<<endl;
		a++;
	
	} while(a < 5);
	

//CONTOH WHILE
cout<<"CONTOH WHILE"<<endl;
int i = 0;
while (i < 5) {
  cout << i << "\n";
  i++;
}  
  return 0;
}